# Leia o valor da base e altura de um triângulo, calcule e escreva sua área. (área=(base * altura)/2)
# Entrada
base = int(input('Valor da base do triângulo: '))
altura = int(input('Valor da altura do triângulo: '))

# Processamento
area = (base * altura)/2

# Saída
print('A área do triângulo é {}'.format(area))