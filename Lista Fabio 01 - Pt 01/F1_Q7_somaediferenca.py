# Entrada
numero_1 = int(input('Primeiro número: '))
numero_2 = int(input('Segundo número: '))
numero_3 = int(input('Terceiro número: '))

# Processamento
soma = numero_1 + numero_2
diferenca = numero_2 - numero_3

# Saída
print('A soma dos primeiros números é {} e a diferença entre os últimos é {}'.format(soma, diferenca))